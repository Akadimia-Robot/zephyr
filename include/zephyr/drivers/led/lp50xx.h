#ifndef ZEPHYR_INCLUDE_DRIVERS_LED_LP50XX_H_
#define ZEPHYR_INCLUDE_DRIVERS_LED_LP50XX_H_

#define LP5009_MAX_LEDS			3
#define LP5012_MAX_LEDS			4
#define LP503X_MAX_LEDS			12
#define LP50XX_COLORS_PER_LED		3

/*
 * LED channels mapping.
 */

#define LP503X_NUM_CHANNELS		52
#define LP5009_NUM_CHANNELS		17
#define LP5012_NUM_CHANNELS		20

/* Bank channels. */
#define LP50XX_BANK_CHAN_BASE		0
#define LP50XX_BANK_BRIGHT_CHAN		LP50XX_BANK_CHAN_BASE
#define LP50XX_BANK_COL1_CHAN		(LP50XX_BANK_CHAN_BASE + 1)
#define LP50XX_BANK_COL2_CHAN		(LP50XX_BANK_CHAN_BASE + 2)
#define LP50XX_BANK_COL3_CHAN		(LP50XX_BANK_CHAN_BASE + 3)

/* LED color channels. */
#define LP5009_12_LED_COL_CHAN_BASE	 8
#define LP5009_12_LED_COL1_CHAN(led) \
	(LP5009_12_LED_COL_CHAN_BASE + led * LP50XX_COLORS_PER_LED)
#define LP5009_12_LED_COL2_CHAN(led) \
	(LP5009_12_LED_COL_CHAN_BASE + led * LP50XX_COLORS_PER_LED + 1)
#define LP5009_12_LED_COL3_CHAN(led) \
	(LP5009_12_LED_COL_CHAN_BASE + led * LP50XX_COLORS_PER_LED + 2)

#define LP503X_LED_COL_CHAN_BASE	16
#define LP503X_LED_COL1_CHAN(led)	(LP503X_LED_COL_CHAN_BASE + \
					 led * LP50XX_COLORS_PER_LED)
#define LP503X_LED_COL2_CHAN(led)	(LP503X_LED_COL_CHAN_BASE + \
					 led * LP50XX_COLORS_PER_LED + 1)
#define LP503X_LED_COL3_CHAN(led)	(LP503X_LED_COL_CHAN_BASE + \
					 led * LP50XX_COLORS_PER_LED + 2)

/* LED brightness channels. */
#define LP5009_LED_BRIGHT_CHAN_BASE	3
#define LP5009_LED_BRIGHT_CHAN(led)	\
	(LP5009_LED_BRIGHT_CHAN_BASE + led)

#define LP5012_LED_BRIGHT_CHAN_BASE	3
#define LP5012_LED_BRIGHT_CHAN(led)	\
	(LP5012_LED_BRIGHT_CHAN_BASE + led)

#define LP503X_LED_BRIGHT_CHAN_BASE	4
#define LP503X_LED_BRIGHT_CHAN(led)	\
	(LP503X_LED_BRIGHT_CHAN_BASE + led)

#endif /* ZEPHYR_INCLUDE_DRIVERS_LED_LP50XX_H_ */
